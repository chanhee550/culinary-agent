def get_lang() -> str:
    return st.session_state.get("lang", "ko")


def t(key: str) -> str:
    lang = get_lang()
    return TRANSLATIONS.get(lang, TRANSLATIONS["ko"]).get(key, key)


def language_selector():
    """사이드바에 언어 선택기 배치"""
    langs = {"ko": "🇰🇷 한국어", "en": "🇺🇸 English"}
    current = get_lang()
    selected = st.sidebar.selectbox(
        t("language"),
        list(langs.keys()),
        index=list(langs.keys()).index(current),
        format_func=lambda x: langs[x],
        key="lang_selector",
    )
    if selected != current:
        st.session_state["lang"] = selected
        st.rerun()
